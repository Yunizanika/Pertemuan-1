fun main() {
    // mendeklarkan variabel

    // literal dengan tipe data integer
    var intLiteral = 5

    // literal dengan tipe data double,float(bilangan berkoma)
    var doubleLiteral = .02

    // literal dengan tipe data string
    var stringLiteral = "Hello"

    // literal dengan tipe data character
    var charLiteral = '1'

    // literal dengan tipe data boolean
    var boolLiteral = true

    // mencetak masing masing literal dengan tipe yang berbeda
    println("Int Literal = $intLiteral")
    println("Double Literal = $doubleLiteral")
    println("String Literal = $stringLiteral")
    println("Char Literal = $charLiteral")
    println("Boolean Literal = $boolLiteral")
}